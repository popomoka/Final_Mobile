package com.example.projet_final_mobile;

public class Monster {
    public int dammage;
    public int armor;
    public int pv;
    public int dodgechance;
    public int critchance;

    public Monster(int dammage_p, int armor_p,  int pv_p,  int dodgechance_p,  int critchance_p)
    {
        dammage = dammage_p;
        armor = armor_p;
        pv = pv_p;
        dodgechance = dodgechance_p;
        critchance = critchance_p;
    }
}


